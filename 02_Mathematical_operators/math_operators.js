// JavaScript Mathematical Operators
// + -> Addition
// - -> Subtraction
// * -> Multiplication
// / -> Division
// % -> Modulus(Remainder)
// ** -> Exponentiation

// Addition
let age = 10 + 23;
console.log(age);

// Subtraction
let points = 50 - 19;
console.log(points);

// Multiplication
let score = 7 * 58;
console.log(score);

// Division
let divide = 100 / 13;
console.log(divide);

// Modulus (remainder)
let mod = 68 % 3;
console.log(mod);

// Exponentiation

let exp = 4 ** 3;
console.log(exp);
