// JavaScript Mathematical Assignment Operators
// ++ -> Increment by 1
// -- -> Decrement by 1
// += -> Increment by value
// -= -> Decrement by value
// *= -> Multiply by value
// /= -> Divide by value
// %= -> Modulus by value
// **= -> Exponentiate by value

let points = 10;
points++; // points = points + 1
console.log(points);

let score = 20;
score--; // points = points -1
score--;
console.log(score);

score += 100;
console.log(score);
score -= 50;
console.log(score);

let age = 7;
age *= 2; // age = age * 2
console.log(age);

age /= 2;
console.log(age);

// age is 7 here
// age %= 7; // age = age % 7
// console.log(age);

age **= 2; // age = age ** 2
console.log(age);
