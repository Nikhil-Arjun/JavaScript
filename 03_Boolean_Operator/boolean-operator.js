// JS Boolean Operators
// == -> Equal to
// === -> Strict Equal to
// != -> Not Equal to
// !==  -> Strict Not Equal to
// > -> Greater than
// < -> Less than
// >= -> Greater than or equal to
// <= -> Less than or equal to
// && -> Logical AND
// || -> Logical OR
// ?? -> Nullish Coalescing

// Strict Equality
// ligatures
console.log(5 === 5);
console.log(5 === 4);
console.log(4 === 5);

console.log("------------");
// Strict Not Equal
console.log(5 !== 5);
console.log(5 !== 4);
console.log(4 !== 5);

console.log("------------");
// Boolean "mathematical" Operator
console.log(9 > 7);
console.log(6 < 5);
console.log(3 < 3);
console.log(3 <= 3);
console.log(4 >= 2);
